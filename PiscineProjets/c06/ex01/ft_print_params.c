#include <unistd.h>

int	main(int gc, char **gv)
{
	int	i;
	int	j;

	(void)gc;
	j = 1;
	while (gv[j])
	{
		i = 0;
		while (gv[j][i] != '\0')
		{
			write(1, &gv[j][i], 1);
			i++;
		}
		write(1, "\n", 1);
		j++;
	}
	return (0);
}
